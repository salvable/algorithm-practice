# 5로 나누거나 3으로 나누거나 2로 나누거나 1을 뺴서 최종값 1로 만드는 최소횟수 DP 문제
x = int(input())

d = [0] * 30001

for i in range(2,x+1):

    # 1을 뺴는 연산 // 이전거에서 1을 더해줌
    d[i] = d[i-1] + 1

    # 2로 나누는 경우
    if i % 2 == 0:
        d[i] = min(d[i], d[i//2] + 1)
    # 3으로 나누는 연산
    if i % 3 == 0:
        d[i] = min(d[i], d[i//3] + 1)
    # 5로 나누는 연산
    if i % 5 == 0:
        d[i] = min(d[i], d[i//5] + 1)

print(d[x])
